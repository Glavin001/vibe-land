export function resolveSuite(name: string): BenchmarkSuiteSpec {
  if (name === 'smoke') return SMOKE_SUITE;
  if (name === 'default') return DEFAULT_SUITE;
  throw new Error(`Unknown benchmark suite: ${name}`);
}
